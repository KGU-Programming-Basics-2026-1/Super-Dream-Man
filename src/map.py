from tile import *


def load_map1_1():
    """Load map1_1 and return a 2D array of Tile objects for '=' and '#'."""
    tile_map = []
    height = len(map1_1)
    for y, row in enumerate(map1_1):
        tile_row = []
        tile_y = height - 1 - y
        for x, ch in enumerate(row):
            if ch == '#':
                tile_row.append(Tile(x, tile_y, TileTypes.TILE_BREAKABLE))
            elif ch == '=':
                tile_row.append(Tile(x, tile_y, TileTypes.TILE_UNBREAKABLE))
        tile_map.append(tile_row)
    return tile_map

